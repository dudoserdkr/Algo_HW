#include <iostream>

// Тут невизначеність у співвідношенні d) при 0^0, тому вважаємо, що сума починається з i = 1

int foo(int n) {
    int sum = 0;
    int var = 1;
    for (int i = 1; i <= n; i++) {
        var = 1;
        for (int j  = 1; j <= i; j++) {
            var *= i;
        }
        sum += var;
    }
    return sum;
}

// Тут я привів алгоритм, що еквівалентний співвідношення d) та виконуєтсья за O(n^2), оскільки маємо цикл в циклі, тому програма виконується за O(n^2)